package com.example.biblio.utils;

public class Repance  {
    String rep1;
    String rep2;
    String rep3;
    String rep4;
    String rep5;


    public Repance() {
    }

    public Repance(String rep1, String rep2, String rep3, String rep4, String rep5) {
        this.rep1 = rep1;
        this.rep2 = rep2;
        this.rep3 = rep3;
        this.rep4 = rep4;
        this.rep5 = rep5;

    }

    public String getRep1() {
        return rep1;
    }

    public void setRep1(String rep1) {
        this.rep1 = rep1;
    }

    public String getRep2() {
        return rep2;
    }

    public void setRep2(String rep2) {
        this.rep2 = rep2;
    }

    public String getRep3() {
        return rep3;
    }

    public void setRep3(String rep3) {
        this.rep3 = rep3;
    }

    public String getRep4() {
        return rep4;
    }

    public void setRep4(String rep4) {
        this.rep4 = rep4;
    }

    public String getRep5() {
        return rep5;
    }

    public void setRep5(String rep5) {
        this.rep5 = rep5;
    }
}
